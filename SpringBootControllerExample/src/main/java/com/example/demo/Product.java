package com.example.demo;

public class Product {
    private int id;
    private double price;
    private String name;
    private String categoryName;

    public int getId() {
        return id;
    }

    public double getPrice() {
        return price;
    }

    public String getName() {
        return name;
    }

    public String getCategoryName() {
        return categoryName;
    }
}
